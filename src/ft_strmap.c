#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*p;
	char	*map;

	map = ft_strdup(s);
	if (map)
	{
		p = map;
		while (*p)
			*p++ = f(*s++);
	}
	return (map);
}
