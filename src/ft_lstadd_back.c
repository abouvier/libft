#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*last;

	last = ft_lstlast(*alst);
	if (last)
	{
		last->next = new;
		new->prev = last;
	}
	else
		*alst = new;
}
