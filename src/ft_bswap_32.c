#include "libft.h"

unsigned	ft_bswap_32(unsigned x)
{
	unsigned	v;

	v = x << 24;
	v |= (x & 0xff00) << 8;
	v |= (x >> 8) & 0xff00;
	v |= x >> 24;
	return (v);
}
