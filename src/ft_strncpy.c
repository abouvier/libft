#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*p;

	p = s1;
	while (*s2 && n)
	{
		*p++ = *s2++;
		n--;
	}
	while (n--)
		*p++ = '\0';
	return (s1);
}
