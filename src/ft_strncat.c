#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*p;

	p = s1;
	while (*p)
		p++;
	while (*s2 && n--)
		*p++ = *s2++;
	*p = '\0';
	return (s1);
}
